package model;

public class Departamento {
	private int departamento_id;
	private String nome;
	
	public int getId() { return departamento_id; }
	public void setId(int departamento_id) { this.departamento_id = departamento_id; }
	
	public String getNome() { return nome; }
	public void setNome(String nome) { this.nome = nome; }
}
